export const countByusers = async (req, res, next) => {
  const users = req.query.users.split(",");
  try {
    const list = await Promise.all(
      users.map((email) => {
        return UserModal.countDocuments({ email : email });
      })
    );
    res.status(200).json(list);
  } catch (err) {
    next(err);
  }
  
};